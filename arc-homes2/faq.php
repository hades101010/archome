<h1>TATTOO FAQs – You should know about.</h1>
<p style="font-family:'Questrial', sans-serif !important; color:#515151 !important;  font-size:15px !important;">


History: The history of tattoo’s began over 5000 years ago and is as diverse as the people who wear them. Tattoos are created by inserting coloured materials beneath the skins surface (more commonly known as ink – these days). The first tattoos probably were created by accident. Someone with a small wound, rubbed it with a hand that was dirty with soot and ashes from the fire. Once the wound had healed, they saw a mark that was unable to be removed and became permanent on their skin. Before deciding on any tattoos the following information will give you an informative idea as to some of the most common questions associated with getting a tattoo. If you have any further questions please do not hesitate to email us.<br/><br/>


<span style="color:#36C;">What is the age limit for getting a tattoo? </span><br/>
The age limit on getting a tattoo is 18 years and over. Identification may be required. Please accept this as a compliment because we thing you look younger than your actual birth age.<br/><br/>

<span style="color:#36C;">Do I have to make an appointment for a tattoo?</span><br/>
Confirm your booking with a deposit is preferred but walk-ins are always welcome.<br/><br/>

<span style="color:#36C;">Can I bring in my own design?</span><br/>
Yes! We encourage people to bring in their own designs as body art is about self expression. We also specialise custom designing your next tattoo each tattooist employed at Eaglehawk Tattoo Parlour is a practising artist currently studying art or have already studied and we are all trade qualified or currently in an apprenticeship, we also offer free-hand tattooing.<br/><br/>

<span style="color:#36C;">How much to Tattoos cost?</span><br/>
Our minimum cost is $100. Our normal hourly rate is $100 per hour we cannot give quotes over the phone on self designed artwork as we cannot see them visually. The cost of a tattoo depends on the size and detail, which our artists will be happy to quote-in-store for you.<br/><br/>

<span style="color:#36C;">What is the age limit for getting a tattoo?</span><br/>
The age limit on getting a tattoo is 18 years and over. Identification may be required. Please accept this as a compliment because we thing you look younger than your actual birth age.<br/><br/>

<span style="color:#36C;">Will it hurt?</span><br/>
We hear different reactions from everyone. It all depends on your own pain threshold. The most common description we hear is uncomfortable but not all bad. Many have quoted the finished artwork to a mild case of sunburn.<br/><br/>

<span style="color:#36C;">How long will it take to heal and what care should be taken?</span><br/>

Tattoos take approximately 7 to 14 days to heal &amp; during this time you do have to follow certain instructions, keep the tattoo clean, avoid swimming and sunburn. A light scab may form and under no circumstances are you allowed to scratch or pick at it. We recommend After inked cream for your aftercare but you must wash your hands with antibacterial soap before applying the cream.<br/><br/>

<span style="color:#36C;">What is your guarantee?</span><br/>


We guarantee our work for six months. We understand that even though you have looked after your tattoo it might require a touch-up. You MUST follow instructions given to you by your artist. We cover our mistakes, not yours through neglect. We can tell the difference.<br/><br/>












